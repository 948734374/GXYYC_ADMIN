package com.mcfish.entity.common;

import java.io.Serializable;
import java.util.Date;

/**
 * 用户实体类
 * @author WangHaibo
 * @date 2018年4月19日 下午2:03:05
 * @version 1.0
 */
public class User implements Serializable{
	
	/**
	 * 序列化
	 */
	private static final long serialVersionUID = -8165818632919833725L;

	private int id;							//用户ID
	private String name;					//用户昵称
	private String head;					//用户的头像
	private int sex;						//用户性别：0-未知  1-男 2-女
	private int identity;					//用户身份位
	private String phone;					//绑定的手机号
	private String password;				//用户登陆密码
	private String openid_wx;				//绑定微信登陆
	private int user_coupon;				//优惠券
	private int order_number;				//订单数
 	private int money;						//用户钱包余额：单位分
	private String comment;					//备注，冻结的时候可以备注原因
	private int status;						//用户状态：0-正常 1-冻结
	private Date login_time;				//用户登陆时间
	private Date create_time;				//注册时间
	private Long total;						//统计数量时使用
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public int getIdentity() {
		return identity;
	}
	public void setIdentity(int identity) {
		this.identity = identity;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getOpenid_wx() {
		return openid_wx;
	}
	public void setOpenid_wx(String openid_wx) {
		this.openid_wx = openid_wx;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public Date getLogin_time() {
		return login_time;
	}
	public void setLogin_time(Date login_time) {
		this.login_time = login_time;
	}
	public Date getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}
	public Long getTotal() {
		return total;
	}
	public void setTotal(Long total) {
		this.total = total;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	public int getUser_coupon() {
		return user_coupon;
	}
	public void setUser_coupon(int user_coupon) {
		this.user_coupon = user_coupon;
	}
	public int getOrder_number() {
		return order_number;
	}
	public void setOrder_number(int order_number) {
		this.order_number = order_number;
	}
	@Override
	public String toString() {
		return "User [id=" + id + ", name=" + name + ", head=" + head + ", sex=" + sex + ", identity=" + identity
				+ ", phone=" + phone + ", password=" + password + ", openid_wx=" + openid_wx + ", user_coupon="
				+ user_coupon + ", order_number=" + order_number + ", money=" + money + ", comment=" + comment
				+ ", status=" + status + ", login_time=" + login_time + ", create_time=" + create_time + ", total="
				+ total + "]";
	}
	
}
